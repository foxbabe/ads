package com.sztouyun.advertisingsystem.viewmodel.advertisement;


public class AdvertisementMaterialPositionViewModel {

    private String advertiementId;

    private String url;

    private String positionType;

    public String getAdvertiementId() {
        return advertiementId;
    }

    public void setAdvertiementId(String advertiementId) {
        this.advertiementId = advertiementId;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getPositionType() {
        return positionType;
    }

    public void setPositionType(String positionType) {
        this.positionType = positionType;
    }
}
